package com.andrewgood.appznov1201.adapters;

public class CourseItemClass {
    String title;
    String unitsCount;
    String lessonsCount;
    String testsCount;
    int imageId;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUnitsCount() {
        return unitsCount;
    }

    public void setUnitsCount(String unitsCount) {
        this.unitsCount = unitsCount;
    }

    public String getLessonsCount() {
        return lessonsCount;
    }

    public void setLessonsCount(String lessonsCount) {
        this.lessonsCount = lessonsCount;
    }

    public String getTestsCount() {
        return testsCount;
    }

    public void setTestsCount(String testsCount) {
        this.testsCount = testsCount;
    }

    public int getImageId() {
        return imageId;
    }

    public void setImageId(int imageId) {
        this.imageId = imageId;
    }
}
